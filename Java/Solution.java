import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        String ln;
        int i = 1;
        while(input.hasNext())
        {
            ln = input.nextLine();
            System.out.println(i + " "+ ln);
            i++;
        }
    }
}


